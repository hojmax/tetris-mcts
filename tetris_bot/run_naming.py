def short_utc_timestamp(now: datetime | None = None) -> str:
    """Return ``YYYYMMDD-HHMM`` (UTC, minute precision)."""
    if now is None:
        now = datetime.now(timezone.utc)
    return now.strftime("%Y%m%d-%H%M")


def generate_run_id(
    *, rng: random.Random | None = None, now: datetime | None = None
) -> str:
    """Generate ``<adjective>-<animal>-<YYYYMMDD-HHMM>``."""
    if rng is None:
        rng = random.SystemRandom()
    return f"{rng.choice(ADJECTIVES)}-{rng.choice(ANIMALS)}-{short_utc_timestamp(now)}"


def is_friendly_run_id(name: str) -> bool:
    """Heuristic: does this look like an `<adj>-<animal>-<YYYYMMDD-HHMM>` id?"""
    parts = name.split("-")
    if len(parts) != 4:
        return False
    adj, animal, date, time = parts
    return (
        adj in ADJECTIVES
        and animal in ANIMALS
        and len(date) == 8
        and date.isdigit()
        and len(time) == 4
        and time.isdigit()
    )
